
class Planta {
  late String nomePopular;
  late String nomeCientifico;
  late String classe;
  late String ordem;
  late String familia;
  late String genero;
  late String origem;
  late String tipo;
  late String tamanho;
  late String dificuldade;
  late String crescimento;
  late String iluminacao;
  late String substrato;
  late String phAgua;
  late String temperatura;
  late String imagem;

  Planta(
      String nomePopular,
      String nomeCientifico,
      String classe,
      String ordem,
      String familia,
      String genero,
      String origem,
      String tipo,
      String tamanho,
      String dificuldade,
      String crescimento,
      String iluminacao,
      String substrato,
      String phAgua,
      String temperatura,
      String imagem) {
    this.nomePopular = nomePopular;
    this.nomeCientifico = nomeCientifico;
    this.classe = classe;
    this.ordem = ordem;
    this.familia = familia;
    this.genero = genero;
    this.origem = origem;
    this.tipo = tipo;
    this.tamanho = tamanho;
    this.dificuldade = dificuldade;
    this.crescimento = crescimento;
    this.iluminacao = iluminacao;
    this.substrato = substrato;
    this.phAgua = phAgua;
    this.temperatura = temperatura;
    this.imagem = imagem;
  }
}
